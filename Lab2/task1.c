#include <stdio.h>


int main(void)
{
    
    /* FOR LOOP
    for (int i = 100; i>-1; i--)
    {
        printf("%d \n", i);
    }
    */

   /* WHILE LOOP
   int i = 100;
   while (i > -1)
   {
    printf("%d \n", i);
    i--;
   }
   */

/* DO-WHILE LOOP*/
  int i = 100;
  do {
    printf("%d \n", i);
    i--;
  } while (i>=0);

}